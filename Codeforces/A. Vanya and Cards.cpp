void solve(void)
{
    ll n, x, i, sum = 0, res;
    cin >> n >> x;
    while (n--)
    {
        cin >> i;
        sum += i;
    }
    sum = abs(sum);
    res = sum / x;
    if (sum % x != 0)
        res++;
    cout << res;
}
