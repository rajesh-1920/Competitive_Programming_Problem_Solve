void solve()
{
    int x = 0, n, s, mx = 0;
    cin >> n;
    for (int i = 2; i <= n; i++)
    {
        s = 0;
        for (int j = 1;; j++)
        {
            if (i * j > n)
                break;
            s += (i * j);
            if (s >= mx)
            {
                mx = s;
                x = i;
            }
        }
    }
    cout << x;
    nl;
}
