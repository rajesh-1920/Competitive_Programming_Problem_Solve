void solve(void)
{
    int k,t,cnt=0;
    cin>>t>>k;
    while (t--)
    {
        if(5*t+5<=k)k-=5;
        else if(4*t+4<=k)k-=4;
        else if(3*t+3<=k)k-=3;
        else
        {
            cnt++;
            k-=2;
        }
    }
    cout<<cnt;
}
